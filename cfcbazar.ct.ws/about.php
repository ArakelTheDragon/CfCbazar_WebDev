    <h2>The WorkToken System</h2>
    <p>Our WorkToken model merges blockchain flexibility with platform-specific rewards. We have bought an amount of Blockchain WorkTokens which is the reserve that we use for platform credits to award users in games & features. There are two distinct forms:</p>
    <ul>
      <li><strong>Blockchain WorkTokens:</strong> Cryptographic tokens you can buy, sell, and trade using our decentralized <a href="https://cc.free.bg/workth/">app</a> (dApp) with wallets like MetaMask. Fully transferable and tradable on-chain.</li>
      <li><strong>Platform Credit WorkTokens:</strong> Internal credits for in-platform purchases, games, and features. Not directly redeemable outside CfCbazar. Convertible to blockchain WorkTokens under certain conditions.</li>
    </ul>
    <p><em>Tip:</em> For trading Blockchain WorkTokens, visit our <a href="https://cc.free.bg/workth/">Smart Contract page</a>. For platform credit rules, see our <a href="/t.php">Terms & Conditions</a>.</p>

